package model;

import java.io.Serializable;

/**
 *
 * @author lizpu
 */
public class Articulo implements Serializable{
    private int id;
    private String nombre;
    private String precio;
    private int stock;
   
    public Articulo() {
    }

    public Articulo(int id, String nombre, String precio, int stock) {
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.stock = stock;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
     
}
